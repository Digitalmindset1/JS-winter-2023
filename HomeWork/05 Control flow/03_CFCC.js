// Write a program based on a letter and displays whether the letter is a vowel or a consonant using an if-else statement. let letter = 'e';

let letter = 'e';
if (letter === 'a' || letter === 'e' || letter === 'i' || letter === 'o' || letter === 'u') {
    console.log(letter + ' is a vowel');
} else {
    console.log(letter + ' is a consonant')
}